// Combined registry of all crypto buildings.

export const ALL_CRYPTO_BUILDINGS: Record<string, CryptoBuildingDefinition> = {
  ...DEFI_BUILDINGS,
  ...EXCHANGE_BUILDINGS,
  ...CHAIN_BUILDINGS,
  ...CT_BUILDINGS,
  ...MEME_BUILDINGS,
  ...PLASMA_BUILDINGS,
  ...STABLECOIN_BUILDINGS,
  ...INFRASTRUCTURE_BUILDINGS,
  ...LEGENDS_BUILDINGS,
};

// Get crypto building by ID
export function getCryptoBuilding(id: string): CryptoBuildingDefinition | undefined {
  return ALL_CRYPTO_BUILDINGS[id];
}

// Get all crypto buildings by category
export function getCryptoBuildingsByCategory(category: CryptoCategory): CryptoBuildingDefinition[] {
  return Object.values(ALL_CRYPTO_BUILDINGS).filter(b => b.category === category);
}

// Get all crypto building type IDs (keys of the building registry)
// The actual CryptoBuildingType is derived from these keys in buildingRegistry.ts
export function getCryptoBuildingTypes(): string[] {
  return Object.keys(ALL_CRYPTO_BUILDINGS);
}

